static void url_decode(char *dst, const char *src)
{
    while (*src) {
        if (*src == '+') {
            *dst++ = ' ';
            src++;
        } else if (*src == '%' && src[1] && src[2]) {
            char hex[3] = {src[1], src[2], '\0'};
            *dst++ = (char)strtol(hex, NULL, 16);
            src += 3;
        } else {
            *dst++ = *src++;
        }
    }
    *dst = '\0';
}

/* Check that a string contains only printable UTF-8 characters (no control chars 0x00-0x1F except tab/newline) */
static bool is_valid_utf8_printable(const char *s, size_t max_len)
{
    if (!s || !s[0]) return false;
    size_t len = strnlen(s, max_len);
    if (len >= max_len) return false;
    for (size_t i = 0; i < len; i++) {
        unsigned char c = (unsigned char)s[i];
        if (c < 0x20 && c != '\t') return false;
    }
    return true;
}

/* Extract session token from Cookie header.
   WARNING: This is a local-prototype auth scheme.
   Session tokens are random but transmitted as plain cookies.
   Not secure against network sniffing on open Wi-Fi. */
static bool get_session_from_request(httpd_req_t *req, char *token_out, size_t token_len)
{
    size_t buf_len = httpd_req_get_hdr_value_len(req, "Cookie");
    if (buf_len <= 0) return false;

    char *cookie_buf = malloc(buf_len + 1);
    if (!cookie_buf) return false;

    if (httpd_req_get_hdr_value_str(req, "Cookie", cookie_buf, buf_len + 1) != ESP_OK) {
        free(cookie_buf);
        return false;
    }

    /* Simple parse: find "session=" */
    const char *start = strstr(cookie_buf, "session=");
    if (start) {
        start += 8; /* skip "session=" */
        const char *end = strchr(start, ';');
        size_t len = end ? (size_t)(end - start) : strlen(start);
        if (len >= token_len) len = token_len - 1;
        memcpy(token_out, start, len);
        token_out[len] = '\0';
        free(cookie_buf);
        return true;
    }
    free(cookie_buf);
    return false;
}

/* Auth check middleware - returns true if session is valid */
static bool require_auth(httpd_req_t *req)
{
    char token[SESSION_TOKEN_LEN] = {0};
    if (!get_session_from_request(req, token, sizeof(token))) return false;
    return session_validate(token);
}

