static bool host_equals(const char *host_start, size_t host_len, const char *expected)
{
    return expected && strlen(expected) == host_len &&
           strncmp(host_start, expected, host_len) == 0;
}

static bool header_matches_allowed_origin(const char *header)
{
    if (!header || !header[0]) {
        return false;
    }

    const char *host_start = strstr(header, "://");
    host_start = host_start ? host_start + 3 : header;

    const char *host_end = host_start;
    while (*host_end && *host_end != ':' && *host_end != '/' &&
           *host_end != '?' && *host_end != '#') {
        host_end++;
    }

    size_t host_len = (size_t)(host_end - host_start);
    if (host_len == 0) {
        return false;
    }

    const char *ap_ip = wifi_manager_get_ap_ip();
    if (host_equals(host_start, host_len, ap_ip)) {
        return true;
    }

    const char *sta_ip = wifi_manager_get_sta_ip();
    if (sta_ip && sta_ip[0] && host_equals(host_start, host_len, sta_ip)) {
        return true;
    }

    return host_equals(host_start, host_len, APP_TEMPLATE_MDNS_HOSTNAME ".local");
}

/* Check Origin/Referer to prevent cross-site POST (CSRF mitigation) */
static bool is_same_origin(httpd_req_t *req, bool allow_missing_header)
{
    size_t origin_len = httpd_req_get_hdr_value_len(req, "Origin");
    size_t referer_len = httpd_req_get_hdr_value_len(req, "Referer");
    char buf[128] = {0};

    if (origin_len > 0) {
        if (origin_len >= sizeof(buf)) return false;
        httpd_req_get_hdr_value_str(req, "Origin", buf, sizeof(buf));
    } else if (referer_len > 0) {
        if (referer_len >= sizeof(buf)) return false;
        httpd_req_get_hdr_value_str(req, "Referer", buf, sizeof(buf));
    } else {
        ESP_LOGW(TAG, "POST request without Origin/Referer header");
        return allow_missing_header;
    }

    if (header_matches_allowed_origin(buf)) return true;

    ESP_LOGW(TAG, "Cross-origin POST blocked: %s", buf);
    return false;
}

static bool read_request_body(httpd_req_t *req, char *body, size_t body_size)
{
    if (!req || !body || body_size == 0 || req->content_len == 0 ||
        req->content_len >= body_size) {
        return false;
    }

    size_t received = 0;
    int timeout_count = 0;
    while (received < req->content_len) {
        int ret = httpd_req_recv(req, body + received, req->content_len - received);
        if (ret == HTTPD_SOCK_ERR_TIMEOUT) {
            if (++timeout_count >= 3) {
                return false;
            }
            continue;
        }
        if (ret <= 0) {
            return false;
        }
        timeout_count = 0;
        received += (size_t)ret;
    }

    body[received] = '\0';
    return true;
}

/* Send JSON response */
static esp_err_t send_json(httpd_req_t *req, const char *json, const char *status)
{
    set_no_store_response_headers(req);
    httpd_resp_set_type(req, "application/json");
    httpd_resp_set_status(req, status);
    esp_err_t ret = httpd_resp_send(req, json, strlen(json));
    if (ret != ESP_OK) {
        uint32_t failure_count = increment_json_send_failures();
        ESP_LOGW(TAG, "JSON response send failed: uri=%s fd=%d err=%s count=%lu",
                 req->uri, httpd_req_to_sockfd(req), esp_err_to_name(ret),
                 (unsigned long)failure_count);
        return ret;
    }
    return ret;
}

static esp_err_t send_api_error(httpd_req_t *req, const char *code, const char *message,
                                const char *status)
{
    cJSON *root = cJSON_CreateObject();
    if (!root) {
        return send_json(req, "{\"ok\":false,\"error\":\"memory\",\"message\":\"JSON allocation failed\"}",
                         "500 Internal Server Error");
    }

    cJSON_AddBoolToObject(root, "ok", false);
    cJSON_AddStringToObject(root, "error", code);
    cJSON_AddStringToObject(root, "message", message);

    char *json_str = cJSON_PrintUnformatted(root);
    cJSON_Delete(root);
    if (!json_str) {
        return send_json(req, "{\"ok\":false,\"error\":\"memory\",\"message\":\"JSON allocation failed\"}",
                         "500 Internal Server Error");
    }

    esp_err_t result = send_json(req, json_str, status);
    free(json_str);
    return result;
}

static esp_err_t send_api_error_with_pump_status(httpd_req_t *req, const char *code,
                                                 const char *message, const char *status)
{
    cJSON *root = cJSON_CreateObject();
    if (!root) {
        return send_api_error(req, "memory", "JSON allocation failed", "500 Internal Server Error");
    }

    cJSON_AddBoolToObject(root, "ok", false);
    cJSON_AddStringToObject(root, "error", code);
    cJSON_AddStringToObject(root, "message", message);
    api_pump_add_status_object(root);

    char *json_str = cJSON_PrintUnformatted(root);
    cJSON_Delete(root);
    if (!json_str) {
        return send_api_error(req, "memory", "JSON allocation failed", "500 Internal Server Error");
    }

    esp_err_t result = send_json(req, json_str, status);
    free(json_str);
    return result;
}

