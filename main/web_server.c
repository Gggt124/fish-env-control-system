/* GET /api/wifi/scan - protected, returns JSON scan results */
static esp_err_t handle_api_wifi_scan(httpd_req_t *req)
{
    if (!require_auth(req)) {
        return send_json(req, "{\"ok\":false,\"error\":\"unauthorized\"}", "401 Unauthorized");
    }

    wifi_scan_ctx_t ctx;
    ctx.sem = xSemaphoreCreateBinary();
    ctx.count = 0;
    if (!ctx.sem) {
        return send_json(req, "{\"ok\":false,\"error\":\"memory\"}", "500 Internal Server Error");
    }

    if (!wifi_manager_scan(&ctx, scan_complete_cb)) {
        vSemaphoreDelete(ctx.sem);
        return send_json(req, "{\"ok\":false,\"error\":\"scan_failed\"}", "200 OK");
    }

    if (xSemaphoreTake(ctx.sem, pdMS_TO_TICKS(5000)) != pdTRUE) {
        vSemaphoreDelete(ctx.sem);
        return send_json(req, "{\"ok\":false,\"error\":\"scan_timeout\"}", "200 OK");
    }
    vSemaphoreDelete(ctx.sem);

    cJSON *root = cJSON_CreateObject();
    cJSON_AddBoolToObject(root, "ok", true);
    cJSON *arr = cJSON_AddArrayToObject(root, "networks");
    for (int i = 0; i < ctx.count; i++) {
        cJSON *item = cJSON_CreateObject();
        cJSON_AddStringToObject(item, "ssid", ctx.entries[i].ssid);
        cJSON_AddNumberToObject(item, "rssi", ctx.entries[i].rssi);
        cJSON_AddStringToObject(item, "auth", ctx.entries[i].auth);
        cJSON_AddNumberToObject(item, "channel", ctx.entries[i].channel);
        cJSON_AddItemToArray(arr, item);
    }

    char *json_str = cJSON_PrintUnformatted(root);
    cJSON_Delete(root);
    httpd_resp_set_type(req, "application/json");
    httpd_resp_send(req, json_str, strlen(json_str));
    free(json_str);

    return ESP_OK;
}

/* POST /api/wifi/connect - protected, connect to given SSID */
static esp_err_t handle_api_wifi_connect(httpd_req_t *req)
{
    if (!require_auth(req)) {
        return send_json(req, "{\"ok\":false,\"error\":\"unauthorized\"}", "401 Unauthorized");
    }

    if (!is_same_origin(req, true)) {
        return send_json(req, "{\"ok\":false,\"error\":\"forbidden\"}", "403 Forbidden");
    }

    char body[512] = {0};
    int ret = httpd_req_recv(req, body, sizeof(body) - 1);
    if (ret <= 0) {
        return send_json(req, "{\"ok\":false,\"error\":\"empty_body\"}", "200 OK");
    }
    body[ret] = '\0';

    char ssid[64] = {0};
    char password[64] = {0};
    wifi_sta_ip_config_t ip_cfg = {0};
    bool has_static_ip = false;

    cJSON *root = cJSON_Parse(body);
    if (root) {
        cJSON *s = cJSON_GetObjectItem(root, "ssid");
        cJSON *p = cJSON_GetObjectItem(root, "password");
        if (s && cJSON_IsString(s)) {
            strncpy(ssid, s->valuestring, sizeof(ssid) - 1);
        }
        if (p && cJSON_IsString(p)) {
            strncpy(password, p->valuestring, sizeof(password) - 1);
        }
        cJSON *ip_item = cJSON_GetObjectItem(root, "ip");
        if (ip_item && cJSON_IsString(ip_item) && ip_item->valuestring[0]) {
            strncpy(ip_cfg.ip, ip_item->valuestring, sizeof(ip_cfg.ip) - 1);
            has_static_ip = true;
            cJSON *gw = cJSON_GetObjectItem(root, "gateway");
            if (gw && cJSON_IsString(gw)) {
                strncpy(ip_cfg.gateway, gw->valuestring, sizeof(ip_cfg.gateway) - 1);
            }
            cJSON *nm = cJSON_GetObjectItem(root, "netmask");
            if (nm && cJSON_IsString(nm)) {
                strncpy(ip_cfg.netmask, nm->valuestring, sizeof(ip_cfg.netmask) - 1);
            }
            cJSON *dns = cJSON_GetObjectItem(root, "dns");
            if (dns && cJSON_IsString(dns)) {
                strncpy(ip_cfg.dns, dns->valuestring, sizeof(ip_cfg.dns) - 1);
            }
        }
        cJSON_Delete(root);
    }

    if (strlen(ssid) == 0) {
        return send_json(req, "{\"ok\":false,\"error\":\"missing_ssid\"}", "200 OK");
    }

    if (strlen(ssid) > 32) {
        return send_json(req, "{\"ok\":false,\"error\":\"ssid_too_long\"}", "400 Bad Request");
    }

    if (!is_valid_utf8_printable(ssid, sizeof(ssid))) {
        return send_json(req, "{\"ok\":false,\"error\":\"invalid_ssid\"}", "400 Bad Request");
    }

    if (strlen(password) > 64) {
        return send_json(req, "{\"ok\":false,\"error\":\"password_too_long\"}", "400 Bad Request");
    }

    if (password[0] && !is_valid_utf8_printable(password, sizeof(password))) {
        return send_json(req, "{\"ok\":false,\"error\":\"invalid_password\"}", "400 Bad Request");
    }

    if (has_static_ip && !ip_cfg.gateway[0]) {
        return send_json(req, "{\"ok\":false,\"error\":\"missing_gateway\"}", "400 Bad Request");
    }

    if (!wifi_manager_connect_sta(ssid, password, has_static_ip ? &ip_cfg : NULL)) {
        return send_json(req, "{\"ok\":false,\"error\":\"connect_error\"}", "200 OK");
    }

    vTaskDelay(pdMS_TO_TICKS(5000));

    cJSON *resp = cJSON_CreateObject();
    if (wifi_manager_is_sta_connected()) {
        cJSON_AddBoolToObject(resp, "ok", true);
        cJSON_AddStringToObject(resp, "ssid", wifi_manager_get_sta_ssid());
        cJSON_AddStringToObject(resp, "ip", wifi_manager_get_sta_ip());
    } else {
        cJSON_AddBoolToObject(resp, "ok", false);
        cJSON_AddStringToObject(resp, "error", "connection_failed");
        cJSON_AddStringToObject(resp, "ssid", ssid);
    }
    char *json_str = cJSON_PrintUnformatted(resp);
    cJSON_Delete(resp);
    esp_err_t result = send_json(req, json_str, "200 OK");
    free(json_str);
    return result;
}

