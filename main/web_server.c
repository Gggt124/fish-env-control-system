static bool api_pump_required_u32(const cJSON *root, const char *name, uint32_t *out,
                                  const char **error_code, const char **error_message)
{
    const cJSON *item = cJSON_GetObjectItem(root, name);
    if (!item) {
        *error_code = "missing_field";
        *error_message = name;
        return false;
    }
    if (!cJSON_IsNumber(item) || !(item->valuedouble >= 0.0)) {
        *error_code = "invalid_type";
        *error_message = name;
        return false;
    }
    if (item->valuedouble < (double)APP_TEMPLATE_PUMP_TIMER_MIN_SEC ||
        item->valuedouble > (double)APP_TEMPLATE_PUMP_TIMER_MAX_SEC) {
        *error_code = "duration_out_of_range";
        *error_message = name;
        return false;
    }

    uint32_t value = (uint32_t)item->valuedouble;
    if ((double)value != item->valuedouble) {
        *error_code = "invalid_type";
        *error_message = name;
        return false;
    }

    *out = value;
    return true;
}

