static bool api_hardware_map_add_json(cJSON *root, const char *name, const hardware_map_t *map)
{
    if (!root || !name || !map) {
        return false;
    }

    cJSON *obj = cJSON_AddObjectToObject(root, name);
    if (!obj) {
        return false;
    }

    cJSON_AddNumberToObject(obj, "float_input_gpio", map->float_input_gpio);
    cJSON_AddNumberToObject(obj, "pump_relay1_gpio", map->pump_relay1_gpio);
    cJSON_AddNumberToObject(obj, "pump_relay2_gpio", map->pump_relay2_gpio);
    cJSON_AddNumberToObject(obj, "ds18b20_gpio", map->ds18b20_data_gpio);
    cJSON_AddNumberToObject(obj, "cooling_relay_gpio", map->cooling_relay_gpio);
    return true;
}

static bool api_hardware_options_add_role(cJSON *options_root, hardware_role_t role)
{
    if (!options_root || !hardware_map_role_valid(role)) {
        return false;
    }

    size_t option_count = 0;
    const hardware_gpio_option_t *options = hardware_map_options_for_role(role, &option_count);
    if (!options) {
        return false;
    }

    cJSON *arr = cJSON_AddArrayToObject(options_root, api_hardware_map_field_for_role(role));
    if (!arr) {
        return false;
    }

    for (size_t i = 0; i < option_count; i++) {
        cJSON *item = cJSON_CreateObject();
        if (!item) {
            return false;
        }
        cJSON_AddNumberToObject(item, "gpio", options[i].gpio);
        cJSON_AddStringToObject(item, "label", options[i].label);
        cJSON_AddStringToObject(item, "role", hardware_map_role_name(options[i].role));
        cJSON_AddBoolToObject(item, "input_capable", options[i].input_capable);
        cJSON_AddBoolToObject(item, "output_capable", options[i].output_capable);
        cJSON_AddBoolToObject(item, "internal_pull_capable", options[i].internal_pull_capable);
        cJSON_AddBoolToObject(item, "is_default", options[i].is_default);
        if (!cJSON_AddItemToArray(arr, item)) {
            cJSON_Delete(item);
            return false;
        }
    }

    return true;
}

static bool api_hardware_map_add_options(cJSON *root)
{
    if (!root) {
        return false;
    }

    cJSON *options = cJSON_AddObjectToObject(root, "options");
    if (!options) {
        return false;
    }

    for (hardware_role_t role = HARDWARE_ROLE_FLOAT_INPUT;
         role < HARDWARE_ROLE_COUNT;
         role = (hardware_role_t)(role + 1)) {
        if (!api_hardware_options_add_role(options, role)) {
            return false;
        }
    }

    return true;
}

static esp_err_t api_hardware_map_send(httpd_req_t *req)
{
    hardware_map_t active = hardware_map_defaults();
    hardware_map_t pending = hardware_map_defaults();
    nvs_store_hardware_map_load_status_t active_status = nvs_store_load_hardware_map(&active);
    nvs_store_pending_hardware_map_load_status_t pending_status =
        nvs_store_load_pending_hardware_map(&pending);

    bool pending_valid = pending_status == NVS_STORE_PENDING_HARDWARE_MAP_LOADED;
    bool reboot_required = false;
    bool reboot_status_ok = nvs_store_hardware_reboot_required(&reboot_required);
    if (!pending_valid) {
        reboot_required = false;
    }

    cJSON *root = cJSON_CreateObject();
    if (!root) {
        return send_api_error(req, "memory", "JSON allocation failed", "500 Internal Server Error");
    }

    cJSON_AddBoolToObject(root, "ok", true);
    cJSON_AddStringToObject(root, "active_status", api_hardware_map_status_name(active_status));
    cJSON_AddStringToObject(root, "pending_status",
                            api_pending_hardware_map_status_name(pending_status));
    cJSON_AddBoolToObject(root, "pending_valid", pending_valid);
    cJSON_AddBoolToObject(root, "reboot_required", reboot_status_ok && reboot_required);
    if (!api_hardware_map_add_json(root, "active", &active)) {
        cJSON_Delete(root);
        return send_api_error(req, "memory", "JSON allocation failed", "500 Internal Server Error");
    }
    if (pending_valid) {
        if (!api_hardware_map_add_json(root, "pending", &pending)) {
            cJSON_Delete(root);
            return send_api_error(req, "memory", "JSON allocation failed", "500 Internal Server Error");
        }
    } else {
        cJSON_AddNullToObject(root, "pending");
    }
    if (!api_hardware_map_add_options(root)) {
        cJSON_Delete(root);
        return send_api_error(req, "memory", "JSON allocation failed", "500 Internal Server Error");
    }

    char *json_str = cJSON_PrintUnformatted(root);
    cJSON_Delete(root);
    if (!json_str) {
        return send_api_error(req, "memory", "JSON allocation failed", "500 Internal Server Error");
    }

    esp_err_t result = send_json(req, json_str, "200 OK");
    free(json_str);
    return result;
}

static const char *api_pump_relay_polarity_name(bool relay_active_low)
{
    return relay_active_low ? "active_low" : "active_high";
}

static bool api_pump_parse_relay_polarity(const cJSON *item, bool *relay_active_low)
{
    if (!item || !cJSON_IsString(item) || !relay_active_low) {
        return false;
    }
    if (strcmp(item->valuestring, "active_low") == 0) {
        *relay_active_low = true;
        return true;
    }
    if (strcmp(item->valuestring, "active_high") == 0) {
        *relay_active_low = false;
        return true;
    }
    return false;
}

static pump_control_relay_polarity_t api_pump_relay_polarity_from_bool(bool active_low)
{
    return active_low ? PUMP_CONTROL_RELAY_ACTIVE_LOW : PUMP_CONTROL_RELAY_ACTIVE_HIGH;
}

static pump_control_start_phase_t api_pump_start_phase_from_hardware(hardware_timer_start_phase_t phase)
{
    return phase == HARDWARE_TIMER_START_PHASE_OFF
        ? PUMP_CONTROL_START_PHASE_OFF
        : PUMP_CONTROL_START_PHASE_ON;
}

static bool api_pump_parse_start_phase(const cJSON *item, hardware_timer_start_phase_t *phase)
{
    if (!item || !cJSON_IsString(item) || !phase) {
        return false;
    }
    if (strcmp(item->valuestring, "on") == 0) {
        *phase = HARDWARE_TIMER_START_PHASE_ON;
        return true;
    }
    if (strcmp(item->valuestring, "off") == 0) {
        *phase = HARDWARE_TIMER_START_PHASE_OFF;
        return true;
    }
    return false;
}

static bool api_pump_load_active_hardware_map(hardware_map_t *map,
                                              nvs_store_hardware_map_load_status_t *status)
{
    if (!map || !status) {
        return false;
    }

    *status = nvs_store_load_hardware_map(map);
    return *status != NVS_STORE_HARDWARE_MAP_DEFAULTS_ERROR;
}

static void api_pump_settings_to_runtime_config(const nvs_store_pump_settings_t *settings,
                                                pump_control_config_t *config)
{
    if (!settings || !config) {
        return;
    }

    *config = pump_control_default_config();
    hardware_map_t active_map = hardware_map_defaults();
    nvs_store_hardware_map_load_status_t map_status = NVS_STORE_HARDWARE_MAP_DEFAULTS_MISSING;
    api_pump_load_active_hardware_map(&active_map, &map_status);
    config->float_gpio = active_map.float_input_gpio;
    config->relay_gpio = active_map.pump_relay1_gpio;
    config->relay1_gpio = active_map.pump_relay1_gpio;
    config->relay2_gpio = active_map.pump_relay2_gpio;
    config->timer1.on_sec = settings->timer1_on_sec;
    config->timer1.off_sec = settings->timer1_off_sec;
    config->timer2.on_sec = settings->timer2_on_sec;
    config->timer2.off_sec = settings->timer2_off_sec;
    config->relay_polarity = api_pump_relay_polarity_from_bool(settings->relay1_active_low);
    config->relay1_polarity = api_pump_relay_polarity_from_bool(settings->relay1_active_low);
    config->relay2_polarity = api_pump_relay_polarity_from_bool(settings->relay2_active_low);
    config->timer1_start_phase =
        api_pump_start_phase_from_hardware(settings->timer1_start_phase);
    config->timer2_start_phase =
        api_pump_start_phase_from_hardware(settings->timer2_start_phase);
}

