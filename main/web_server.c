static bool api_cooling_parse_config_payload(const cJSON *root,
                                             nvs_store_cooling_settings_t *settings,
                                             const char **error_code,
                                             const char **error_message)
{
    if (!root || !settings) {
        *error_code = "invalid_json";
        *error_message = "Expected a JSON object";
        return false;
    }

    if (!api_cooling_required_i32_range(root, "threshold_c_x10", -550, 1250,
                                        &settings->threshold_c_x10,
                                        error_code, error_message) ||
        !api_cooling_required_i32_range(root, "hysteresis_c_x10", 1, 500,
                                        &settings->hysteresis_c_x10,
                                        error_code, error_message) ||
        !api_cooling_required_bool(root, "auto_enable", &settings->auto_enable,
                                   error_code, error_message) ||
        !api_cooling_required_u32_range(root, "test_timeout_sec", 1, 3600,
                                        &settings->test_timeout_sec,
                                        error_code, error_message) ||
        !api_cooling_required_u32_range(root, "compressor_min_off_sec", 0, 86400,
                                        &settings->compressor_min_off_sec,
                                        error_code, error_message)) {
        return false;
    }

    const cJSON *mode = cJSON_GetObjectItem(root, "mode");
    if (!api_cooling_parse_mode(mode, &settings->mode)) {
        *error_code = mode ? "invalid_cooling_mode" : "missing_field";
        *error_message = "mode";
        return false;
    }
    if (settings->mode == HARDWARE_COOLING_MODE_TEST_ON) {
        *error_code = "test_mode_not_persistent";
        *error_message = "Use /api/cooling/mode for runtime Test ON";
        return false;
    }

    const cJSON *polarity = cJSON_GetObjectItem(root, "cooling_relay_polarity");
    if (!api_cooling_parse_polarity(polarity, &settings->relay_polarity)) {
        *error_code = polarity ? "invalid_relay_polarity" : "missing_field";
        *error_message = "cooling_relay_polarity";
        return false;
    }

    return true;
}

static bool api_pump_parse_settings_payload(const cJSON *root,
                                            nvs_store_pump_settings_t *settings,
                                            const char **error_code,
                                            const char **error_message)
{
    if (!root || !settings) {
        *error_code = "invalid_json";
        *error_message = "Expected a JSON object";
        return false;
    }

    const char *readonly_fields[] = {
        "float_gpio",
        "relay_gpio",
        "debounce_ms",
        "pump_relay1_gpio",
        "pump_relay2_gpio",
        "ds18b20_gpio",
        "cooling_relay_gpio",
        "cooling_relay_polarity",
        "hardware_reboot_required"
    };
    for (size_t i = 0; i < sizeof(readonly_fields) / sizeof(readonly_fields[0]); i++) {
        if (api_pump_json_has_field(root, readonly_fields[i])) {
            *error_code = "read_only_field";
            *error_message = readonly_fields[i];
            return false;
        }
    }

    if (!api_pump_required_u32(root, "timer1_on_sec", &settings->timer1_on_sec,
                               error_code, error_message) ||
        !api_pump_required_u32(root, "timer1_off_sec", &settings->timer1_off_sec,
                               error_code, error_message) ||
        !api_pump_required_u32(root, "timer2_on_sec", &settings->timer2_on_sec,
                               error_code, error_message) ||
        !api_pump_required_u32(root, "timer2_off_sec", &settings->timer2_off_sec,
                               error_code, error_message) ||
        !api_pump_required_bool(root, "auto_start", &settings->auto_start,
                                error_code, error_message)) {
        return false;
    }

    const cJSON *relay1_polarity = cJSON_GetObjectItem(root, "relay1_polarity");
    const cJSON *legacy_polarity = cJSON_GetObjectItem(root, "relay_polarity");
    const cJSON *relay1_source = relay1_polarity ? relay1_polarity : legacy_polarity;
    if (!relay1_source) {
        *error_code = "missing_field";
        *error_message = "relay1_polarity";
        return false;
    }
    if (!api_pump_parse_relay_polarity(relay1_source, &settings->relay1_active_low)) {
        *error_code = "invalid_relay_polarity";
        *error_message = relay1_polarity ? "relay1_polarity" : "relay_polarity";
        return false;
    }
    settings->relay_active_low = settings->relay1_active_low;

    const cJSON *relay2_polarity = cJSON_GetObjectItem(root, "relay2_polarity");
    if (relay2_polarity &&
        !api_pump_parse_relay_polarity(relay2_polarity, &settings->relay2_active_low)) {
        *error_code = "invalid_relay_polarity";
        *error_message = "relay2_polarity";
        return false;
    }

    const cJSON *timer1_start = cJSON_GetObjectItem(root, "timer1_start_phase");
    if (!timer1_start) {
        *error_code = "missing_field";
        *error_message = "timer1_start_phase";
        return false;
    }
    if (!api_pump_parse_start_phase(timer1_start, &settings->timer1_start_phase) ||
        !hardware_map_timer_start_phase_valid(settings->timer1_start_phase)) {
        *error_code = "invalid_start_phase";
        *error_message = "timer1_start_phase";
        return false;
    }

    const cJSON *timer2_start = cJSON_GetObjectItem(root, "timer2_start_phase");
    if (!timer2_start) {
        *error_code = "missing_field";
        *error_message = "timer2_start_phase";
        return false;
    }
    if (!api_pump_parse_start_phase(timer2_start, &settings->timer2_start_phase) ||
        !hardware_map_timer_start_phase_valid(settings->timer2_start_phase)) {
        *error_code = "invalid_start_phase";
        *error_message = "timer2_start_phase";
        return false;
    }

    return true;
}

